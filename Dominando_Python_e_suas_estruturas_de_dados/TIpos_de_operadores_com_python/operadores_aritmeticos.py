valor1 = 20
valor2 = 10

# Adição
print(valor1 + valor2)

# Subtração
print(valor1 - valor2)

# Divisão
print(valor1 / valor2)

# Divisão com resultado inteiro
print(valor1 // valor2)

# Multiplicação
print(valor1 * valor2)

# Resto de divisão
print(valor1 % valor2)

# Potenciação
print(valor1 ** valor2)

# Multiplicação primeiro
x = 10 + 5 * 4
print(x)

# Soma (parênteses) primeiro
y = (10 + 5) * 4
print(y)

# Exponenciação, divisão e multiplicação da esquerda pra direita, soma e subtração da esquerda pra direita
z = 10 / 2 + 25 * 2 - 2 ** 2
print(z)

# Parêntesis de dentro pra fora, exponenciação e soma
a = (10 / 2) + ((25 * 2) - 2) ** 2
print(a)