# Atribuir
saldo = 200
print(saldo)

# Sobrescrever
saldo = 500
print(saldo)

# Somar
saldo += 500 # saldo = saldo + 500
print(saldo)

# Subtrair
saldo -= 20 # saldo = saldo - 20
print(saldo)

# Multiplicar
saldo *= 5 # saldo = saldo * 5
print(saldo)

# Dividir (inteiro)
saldo //= 5 # saldo = saldo // 5
print(saldo)

# Dividir
saldo /= 2 # saldo = saldo / 2
print(saldo)

# Resto de divisão
saldo %= 4 # saldo = saldo % 4
print(saldo)

# Potenciação
saldo **= 2 # saldo = saldo ** 2
print(saldo)


